package io.actor4j.analyzer.fx.runtime.visual;

import java.util.UUID;

import com.brunomnsilva.smartgraph.graphview.SmartLabelSource;

public class VertexElement {
	private UUID id;
	private String name;
	
	public VertexElement(String name) {
		super();
		this.id = UUID.randomUUID();
		this.name = name;
	}
	
	@SmartLabelSource
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		VertexElement other = (VertexElement) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "VertexElement [name=" + name + "]";
	}
}
